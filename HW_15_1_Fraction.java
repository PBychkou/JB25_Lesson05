public class HW_15_1_Fraction {
    private int num;
    private int den;

    public HW_15_1_Fraction(){
    }
    
    public HW_15_1_Fraction(int num, int den) {
        this.num = num;
        this.den = den;
    }

    public int getNum() {
        return num;
    }

    public void setNum(int num) {
        this.num = num;
    }

    public int getDen() {
        return den;
    }

    public void setDen(int den) {
        this.den = den;
    }
}